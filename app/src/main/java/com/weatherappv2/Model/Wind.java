package com.weatherappv2.model;

public class Wind {

    private double speed ;
    private double deg ;


    public Wind() {
    }

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public double getDeg() {
        return deg;
    }

    public void setDeg(int deg) {
        this.deg = deg;
    }
}
